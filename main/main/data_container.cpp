bool in_array(std::string value, std::string* array, int size)
{
	std::for_each(value.begin(), value.end(), [](char& c) 
		{
		c = ::toupper(c);
		});


	for (int i = 0; i < size; i++)
	{
		if (value == array[i])
		{
			return true;
		}
	}
	std::cout << "couldn't find pair:" << value << std::endl;
	return false;
}

